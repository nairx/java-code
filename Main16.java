public class Main16 {
    public static void main(String[] args) {
        int[] numbers = { 45, 22, 54, 75 };
        for (int i = 0; i < numbers.length; i++) {
            if (numbers[i] % 2 == 0) {
                System.out.println("Even Number");
            } else {
                System.out.println("Odd Number");
            }
        }
    }
}
