public class Main3 {
    public static void main(String[] args) {
        // Operators: +, -, *, /, ==, !=, &&, ||
        System.out.println(12 + 34);
        System.out.println(24 % 2);
        System.out.println(34 == 23);
        System.out.println(34 != 23);
        System.out.println((13 > 5) && (5 > 2));
        System.out.println((3 > 5) || (5 > 2));

    }
}
